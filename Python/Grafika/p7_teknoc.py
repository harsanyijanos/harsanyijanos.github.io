import turtle
w = turtle.Screen()
w.setup(500,500)
w.bgcolor('white')
w.title("Tekn≈ëcgrafika")
f = turtle.Turtle()
h=200
szog=120
f.width(1)
f.color('red')
f.begin_fill()
f.left(szog/2)
f.fd(h)
f.left(szog)
f.fd(h)
f.left(szog)
f.fd(h)
f.left(szog)
f.end_fill()
f.right(szog/2)

f.color('blue')
f.begin_fill()
f.left(-szog/2)
f.fd(h)
f.left(-szog)
f.fd(h)
f.left(-szog)
f.fd(h)
f.left(-szog)
f.end_fill()
f.right(-szog/2)

f.up()
f.goto(-50,-50)
f.down()
f.color('green', 'yellow')
f.begin_fill()
f.fd(100)
f.left(90)
f.fd(100)
f.left(90)
f.fd(100)
f.left(90)
f.fd(100)
f.left(90)
f.end_fill()

w.exitonclick()