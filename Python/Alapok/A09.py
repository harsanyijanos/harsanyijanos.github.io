a = int(input('Adj meg egy egész számot: '))
b = int(input('Adj meg még egy egész számot: '))
print(a, '+', b, '=', a+b)
print(a, '-', b, '=', a-b)
print(a, '*', b, '=', a*b)
print(a, '/', b, '=', a/b)
